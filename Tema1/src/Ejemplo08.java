
public class Ejemplo08 {

	public static void main(String[] args) {

		boolean a = true;
		boolean b = false;

		// Operación AND.
		System.out.println("AND: " + (a && b));

		// Operación OR.
		System.out.println("OR: " + (a || b));

		// Operación NOT.
		System.out.println("NOT: " + (!b));

	}

}
